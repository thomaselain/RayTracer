#include "libft.h"

int		ft_sqrt(int x)
{
	int		i;

	i = 1;
	if (x == 0)
		return (0);
	if (x == 1)
		return (1);
	while (i <= (x / 2))
	{
		if (ft_pow(i, 2) == x)
			return (i);
		i++;
	}
	return (-1);
}
