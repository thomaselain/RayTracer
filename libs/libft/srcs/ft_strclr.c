#include "libft.h"

void	ft_strclr(char *s)
{
	char	*str;

	if (s == 0)
		return ;
	str = (char *)s;
	while (*str != '\0')
		str++;
	while (str != s)
	{
		*str = '\0';
		str--;
	}
	*str = '\0';
}
